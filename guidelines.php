The inspector MUST call the repair facility to set up an appointment with the technician and to give his or her estimated time of arrival. This is to confirm that the vehicle is there and that it is ready for inspection, including any tear-down requested. It is the inspector’s responsibility to confirm that the vehicle is available and ready for inspection. The inspector should also ask to speak with the contact person provided at the time of assignment, as this is the person who is most likely to have the correct information on the status of the vehicle. Please make a note of the person’s name and the time you called in case there is a discrepancy later.<br />
<br />
All FIA assignments are to be completed within 24 hours. Completed means the inspection performed, and the written, as well as the verbal, report complete. This means, if you receive a claim at 9:00 AM on Monday, you must call the repair shop to set up an appointment for no later than 9:00 AM on Tuesday. Sometimes, the shop will not be ready for you to inspect within the 24 hours (e.g. the vehicle may not be torn down or the shop does not have enough racks.) In that case, call FIA immediately with an estimated time of arrival so that we can advise our client.
