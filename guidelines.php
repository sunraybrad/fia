Reports
</div>
<div class="col-lg-1 col-md-1 col-sm-1 col-xs-1"></div>
</div>

<div class="row" style="margin-top:12px">
<div class="col-lg-1 col-md-1 col-sm-1 col-xs-1"></div>
<div class="col-lg-10 col-md-10 col-sm-10 col-xs-10 body2">
All inspections MUST be done on the supplied FIA report forms, and entered online through the FIA web site. Before leaving the facility, the report should be double checked to be sure the information for each section is completely filled out and is legible.<br /><br />

Please note, FIA has separate forms for auto and marine assignments. Please make sure you use the proper forms.<br /><br />

The ‘Cause of Failure’ and ‘Extent of Damage’ sections of your report are two of the most important sections of both the verbal and the written reports. A concise description of all of the damaged or failed parts involved in the claim is necessary, along with the cause of failure, if any. Please be detailed in your description. Note any related and/or non-related failures. Remember, be objective, and NEVER authorize any tear-down or repairs.<br /><br />

Prior to leaving the shop, your verbal report must be called in to the client and/or FIA (when applicable). If it is not possible to call from the shop, be prepared to return on your own time should the adjuster have any questions that you are not able to answer. The more complete your verbal report, the easier it is for the warranty company to make the correct decision regarding the claim. Give as much information as possible, but report only the FACTS. The verbal report is an important part of the inspection process, but it must be backed up by a thorough written report.<br /><br />

When calling in your verbal report, at times you will unfortunately be placed on hold. Since this is an important part of the inspection process, you will need to have patience when this occurs. If you rush your verbal because you have other inspections to complete, you could omit valuable information. Each inspection is as important as the next.<br /><br />

It is of the utmost importance that the written report matches the verbal report VERBATIM, so it must be complete and precisely accurate. If the final written report does not match the verbal report, this may also delay payment. In some cases this may cancel payment altogether or result in chargebacks. Keep in mind, it only takes one mistake to cause OUR Client to call someone else for the next inspection(s) they need.<br /><br />

When submitting your written report, it should be FAXED to our office the same day - from the shop if possible. If you don't have access to a FAX machine, you should call FIA for further instructions.<br /><br />

FIA requires that the original written report and related 35 mm photos be mailed to our office within seven (7) days of the inspection. If your photos are digital then the should be E-mailed to FIA that evening. The report will then be computerized and forwarded to the warranty company. If the report is incomplete or illegible, it will be returned to you for completion. FIA will not pay for incomplete, illegible or late reports. We process reports in the order they are received, not the order in which the inspections were performed – so please do get your reports in promptly so we may pay you in a timely manner.<br /><br />

All written reports must have the ‘Cause of Failure’ field completed on page 2 of the report form; "UNKNOWN", "POSSIBLE", or other non-conclusive phrases are unacceptable! The report will be returned to you for proper completion of the cause of failure section, if necessary, prior to payment.
