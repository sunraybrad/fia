Inspection Points
</div>
<div class="col-lg-1 col-md-1 col-sm-1 col-xs-1"></div>
</div>

<div class="row" style="margin-top:12px">
<div class="col-lg-1 col-md-1 col-sm-1 col-xs-1"></div>
<div class="col-lg-10 col-md-10 col-sm-10 col-xs-10 body2">
  <ul>
    <li>Head gaskets do not cause overheat; overheat causes head gaskets to fail. If a head gasket is blown, check the head with a straight edge and report your findings; you will most likely find the head is warped if the head gasket is blown. Note the maximum allowable tolerance for machining that engine during your verbal report.</li>
    <li>Always boil the thermostat if overheat is suspected. Report any abnormalities regarding the thermostat, as a stuck thermostat can make a car overheat as well as no thermostat on some engines.</li>
    <li>On all overheat claims, check the water pump for leakage.</li>
    <li>Oil pumps do not fail – parts of oil pumps fail. Always photograph any oil pump problems, such as a stuck relief valve or broken oil pump drive. If a shop tells you that the damage is the result of a bad oil pump, ask them to show you why the oil pump stopped pumping oil – if they can’t show you, don’t report the oil pump as failed.</li>
    <li>Have the shop remove more than one bearing cap for inspection. An accurate cause of failure cannot be determined without visually inspecting the condition of the bearings, rods and main, throughout the entire crank assembly, to verify if the damage is isolated or not.</li>
    <li>You CANNOT authorize tear-down. You CANNOT tell the repair facility to perform any labor that will require payment, but you can ask. Anything more than removing an extra main/rod cap or two, a thermostat, or having an oil pump torn down may require the contract holder’s approval. If the tear-down is authorized and the tear-down time is not excessive, try and wait to inspect what is needed – be reasonable. The more flexible you are, the more valuable you are as an inspector.</li>
    <li>Never reveal the client or claim/contract number. Never discuss whether the repair will be paid or denied. Let the repair facility know it is not your decision – you are only there to report the ‘cause and extent of damage’.</li>
    <li>You must physically see and photograph all damage before reporting it. A good rule of thumb: if you can’t see the alleged damage in order to take a photo of it, you should not include it in your report.</li>
  </ul>
