<div class="col-lg-10 col-md-10 col-sm-10 col-xs-10 body2">Some inspections require photos of the odometer reading and either a rear quarter shot with the vehicle’s license plate or the VIN plate for identification purposes. There must also be enough photos of the failure(s) to document your findings. Sometimes photos of the undamaged parts can be just as important as those of the damaged parts.<br /><br />
      
Keep in mind while going back to the parts list provided by the shop that the shop wants to repair or replace every part on the list. If you can see no damage to any of the alleged damaged parts, you need to support your findings with photos of the parts. If photos of the disputed parts are not provided, you may have to go back to the shop, at your own expense, to obtain the evidence.<br /><br />
      
For inspections where the main purpose is to test drive, remember to take the proper identification photos, if required. ALWAYS have the shop representative drive the vehicle.It is preferred that all inspectors use a digital camera with enough resolution to provide detailed pictures. Digital pictures are to be uploaded for each inspection through the online reporting system or can be e-mailed each night to: pix@fiainspectors.com<br /><br />
  <ul>
    <li>The subject line should be the FIA Inspection Number. Identify the pictures using the FIA # - for example: 10000-1.jpg, 10000-2.jpg, 10000-3.jpg. Otherwise, you can zip the files and name the zip file using the FIA Inspection Number – for example: 10000.zip.</li>
    <li>Please upload pictures for each inspection every night, if possible.</li>
    <li>Reports will not be processed if pictures are not 35 mm or if the digital pictures are not uploaded. Some companies require 35 mm pictures only; if so, you should consider using at least a 28x80 macro-zoom lens.</li>
    <li>Out of focus, overexposed or underexposed photos are NOT acceptable and will be returned.</li>
    <li>If your written report were to be lost or destroyed, the photos would be able to show what has or has not failed. A reduced fee will be paid on inspections that do not have adequate photos to support your findings.</li>
    <li>Since digital cameras are becoming less expensive and the quality of the image is getting better, FIA is suggesting all inspectors upgrade as soon as possible to digital equipment. It will pay for itself in terms of saving you the expense of film and also time, as you will be able to submit your photos quickly and conveniently via our web site or E-mail daily.</li>
  </ul>
