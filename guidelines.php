The following guidelines list the steps that must be taken when performing a mechanical inspection for FIA<br />
<ul>
<li>Upon receiving an inspection assignment from FIA call the shop to confirm the time that you will be there and that the vehicle is ready for inspection.</li>
<li>All inspections must be completed within 24 hours of assignment; if there is going to be a delay please call and let us know so that we can make the proper adjustments. It is very important that we complete these in 24 hours.</li>
<li>Upon going to the shop, contact the service writer and obtain copies of the repair order and service file if available.</li>
<li>Verify the VIN number and the odometer reading from the vehicle. Do not use numbers supplied by the shop, always take them from the vehicle.</li>
<li>Talk to the tech. working on the vehicle about the proposed repairs, look over all related parts and components, check the shop’s conclusion. Do not go along with what the shop says: draw your own conclusions. Confirm the cause and extent of damage.</li>
<li>Look vehicle over, noting any other mechanical problems, and bring them to the attention of the adjuster.</li>
<li>Take photos of all related parts if requested by us or the adjuster, get pix of the odometer, rear quarter of vehicle and any trailer hitches. If there are signs of abuse or neglect prompt the adjuster at the time of the verbal for any photos if not already requested.</li>
<li>Before leaving the shop, complete your written report on the supplied forms and call in your report to the warranty company using the toll free number that we supply. Identify yourself as a FIA inspector, do not use any other company names as this causes confusion, discuss the claim with the adjuster and answer any question that he or she has. Remember that both of you are mechanics, so please have concise technical answers to his or her questions. Remember that your written report and your verbal to the adjuster must match: always do the written report at the shop.</li>
<li>Upon completion, send your report by regular mail to us and include any photos not already sent electronically, properly affixed to the supplied forms and detailed.</li>
<li>If inspections are not completed within specified time limit FIA reserves the right to refuse payment</li>
</ul>
