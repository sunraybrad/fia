The Inspection
</div>
<div class="col-lg-1 col-md-1 col-sm-1 col-xs-1"></div>
</div>

<div class="row" style="margin-top:12px">
<div class="col-lg-1 col-md-1 col-sm-1 col-xs-1"></div>
<div class="col-lg-10 col-md-10 col-sm-10 col-xs-10 body2">
Upon arriving at the shop, ask the technician for copies of the repair order and service file if available. Verify the VIN number and the odometer reading from the vehicle – DO NOT use numbers supplied by the shop. In the course of your inspection, you should also check the vehicle for any leaks or signs of abuse. Verify all the fluids and note them on the inspection report. Are the leaks long-term or short-term? Were there any previous repairs to the vehicle? Check for boil-over stains. Also, check for any collision or accident damage that could have a bearing on this claim or future claims. Check for signs of neglect, modifications, commercial use, off-road use, signs of towing, etc. Should any of these factors be found, your findings must be documented with good, clear pictures. If a claim is denied based on your report, the warranty company will look to your report to substantiate their position. You must present a concise, accurate report with the necessary pictures to back you up.<br /><br />

It is helpful to speak with the technician who is doing the actual repairs on the vehicle, along with the service advisor or contact person given. Request a list of the parts the repair facility wants to replace and use it as a guideline. Have the technician explain why he or she wants to replace each part listed. This information should be included in both your verbal and your written report. Keep in mind that the shop has already advised the client of this parts list prior to your inspection. You are being sent to the shop to validate the need for each and every part on the list. Remember, you are there to confirm the exact cause of failure and extent of damage.<br /><br />

If you should run across damage during the course of your inspection that was not reported to the repair facility, be sure to advise the adjuster of this during your verbal report. It may or may not be of importance, but that is for the client to decide.<br /><br />

After going over all the evidence and the circumstances of the claim, using your mechanical background and experience, you will come to a logical conclusion. If a cause of failure is not obvious, the process of elimination of other possible causes will help determine a logical conclusion.<br /><br />

Sometimes, and for no apparent reason, mechanical devices fail. Make sure that before you report a material failure as the cause of damage, you have checked that it was not caused by overheating, a lubrication problem or may possibly be maintenance or service related. This may be critical in the impact of the claim. Collect the facts on all present conditions to be included in the verbal report. Some of the failures may include sludge, rust, lack of lubrication, corrosion, calcification and overheating. Upon your completion of the inspection, the Service Advisor must sign the written inspection report form before you call in the verbal report.<br /><br />

Once in a while you may come across people who may not be cooperative. You should ALWAYS be as friendly and polite as possible. Make them aware that you are trying to help expedite the claim and the sooner you can get your report completed, the sooner the repairs can be made. If the contact person at the shop is extremely uncooperative, ask to speak with the Service Manager. This usually takes care of the problem. However, if you are still having trouble, call FIA and/or the client, explain what is happening and let us decide on a proper resolution.<br /><br />
