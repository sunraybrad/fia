<div class="col-lg-5 col-md-5 col-sm-5 col-xs-5">
  <div class="h3 text-left">1. Is the appraisal company local?</div>
  <div class="body2" style="margin-top:24px">Typically, you should look for an appraisal company that is active in your home state—they’ll know your market better and will be more familiar with local laws and regulations. Note: websites can be misleading as to a company’s operational location—get verification.<br />
<br />
If you need a local, highly-experienced appraiser in your area, FIA can quickly connect you with a seasoned professional. Just call us at 888-342-4678 for a quote today!</div>
  <div class="col-lg-1 col-md-1 col-sm-1 col-xs-1"></div>
</div>
</div>

<div class="row" style="margin-top:45px">
<div class="col-lg-1 col-md-1 col-sm-1 col-xs-1"></div>
<div class="col-lg-5 col-md-5 col-sm-5 col-xs-5 body2">
<img class="img-responsive" src="/images/Appraisal-SUB/Appraisal-Sub-2.jpg" alt="What are the appraiser’s credentials?"/>
</div>
<div class="col-lg-5 col-md-5 col-sm-5 col-xs-5">
  <div class="h3 text-left">2. What are the appraiser’s credentials?</div>
  <div class="body2" style="margin-top:24px">Beware online-only appraisers, and when “shopping” for an appraisal service, exclusively deal with those that are professionally certified and qualified. (Caution: proper credentials extend beyond a piece of paper. Only a few appraisal certification companies require their members to take courses and pass tests before being admitted.) Find out:<br /><br />
<ul>
<li>From whom did you receive your certification? Beware those who were certified online, and look at the certification company they name. (For the record, FIA is certified by the International Automotive Appraisers Association.)</li>
<li>What other certifications and accreditations do you possess? Ask to see these documents and consider the legitimacy of the organizations that issued them.</li>
<li>Are you licensed, insured and registered to conduct business in Florida? Stay away from fly-by-night operations. Also, find out if they are listed at www.SUNBIZ.ORG as a State of Florida corporation or business.</li>
<li>Have you taken and passed USAAP (Uniformed Standards of Automotive Appraisal Procedures) within the past five years?</li>
<li>Do you belong to of an appraisal society that regularly tests its members and provides continuing education? Procedures and regulations are always changing. In response, FIA constantly updates, expands, and attends classes to ensure that we have knowledge of all the latest professional standards and can expertly perform the work you require.</li>
<li>How long have you been doing appraisals? FIA has been doing this for 30 years!</li>
<li>Are you a full-time appraiser? Part-timers may not be acceptable to lenders, insurance companies or courts.</li>
</ul>
</div>
  <div class="col-lg-1 col-md-1 col-sm-1 col-xs-1"></div>
</div>
</div>

<div class="row" style="margin-top:45px">
<div class="col-lg-1 col-md-1 col-sm-1 col-xs-1"></div>
<div class="col-lg-5 col-md-5 col-sm-5 col-xs-5 body2">
<img class="img-responsive" src="/images/Appraisal-SUB/Appraisal-Sub-3.jpg" alt="What is your fee structure and what does that include?"/>
</div>
<div class="col-lg-5 col-md-5 col-sm-5 col-xs-5">
  <div class="h3 text-left">3. What is your fee structure and what does that include?</div>
  <div class="body2" style="margin-top:24px">Hourly fees, flat rates or per-item charges are acceptable. You shouldn’t hire anyone who charges a percentage of the appraised value, or charges a contingency fee. Such practices represent a conflict of interest, and may result in biased values. (Note that IRS will not accept appraisals done under such fee arrangements.)<br />
<br />
Additionally, the IAAA code of ethics prohibits its certified appraisers from charging a fee based on a percentage of the value of the property appraised. Additionally, find out if the appraiser will charge extra to defend an appraisal or to conduct an appraisal-cause procedure. Finally, appraisals should always include a formal, type written report including photos that presents the information you need in a complete and organized way.</div>
  <div class="col-lg-1 col-md-1 col-sm-1 col-xs-1"></div>
</div>
